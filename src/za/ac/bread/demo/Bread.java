package za.ac.bread.demo;

public class Bread {
	private int weight;
	private String type;
	public Bread(int weight, String type) {
		super();
		this.weight = weight;
		this.type = type;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
